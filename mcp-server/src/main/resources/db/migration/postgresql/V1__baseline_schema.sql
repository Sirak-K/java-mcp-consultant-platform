-- consultant_platform.mission_slot
-- -------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS consultant_platform.mission_slot (
    mission_slot_id               BIGINT      NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    mission_id                    BIGINT      NOT NULL,
    role_id                            BIGINT      NOT NULL,
    required_role_experience_years     SMALLINT    NOT NULL DEFAULT 2,
    req_competency_level_id            SMALLINT    NOT NULL DEFAULT 2,
    mission_slot_number                INT         NOT NULL DEFAULT 1,
    mission_slot_fill_status           VARCHAR(20) NOT NULL DEFAULT 'NOT_FILLED',
    mission_slot_filled_by_profile_id  BIGINT      NULL,
    mission_slot_filled_at             TIMESTAMPTZ NULL,
    PRIMARY KEY (mission_slot_id),
    CONSTRAINT uk_mission_slot_number UNIQUE (mission_id, mission_slot_number),
    CONSTRAINT chk_mission_slot_number_positive CHECK (mission_slot_number >= 1),
    CONSTRAINT chk_mission_slot_required_role_experience_years CHECK (required_role_experience_years >= 0),
    CONSTRAINT fk_mission_slot_req_competency_level FOREIGN KEY (req_competency_level_id) REFERENCES consultant_platform.competency_level_lookup (competency_level_lookup_id),
    CONSTRAINT chk_mission_slot_fill_status CHECK (mission_slot_fill_status IN ('NOT_FILLED', 'FILLED')),
    CONSTRAINT chk_mission_slot_fill_state_consistency CHECK (
        (mission_slot_fill_status = 'NOT_FILLED'
            AND mission_slot_filled_by_profile_id IS NULL
            AND mission_slot_filled_at IS NULL)
        OR
        (mission_slot_fill_status = 'FILLED'
            AND mission_slot_filled_by_profile_id IS NOT NULL
            AND mission_slot_filled_at IS NOT NULL)
    ),
    CONSTRAINT fk_mission_slot_mission FOREIGN KEY (mission_id) REFERENCES consultant_platform.mission (mission_id),
    CONSTRAINT fk_mission_slot_role FOREIGN KEY (role_id) REFERENCES consultant_platform.role (role_id),
    CONSTRAINT fk_mission_slot_filled_by_profile FOREIGN KEY (mission_slot_filled_by_profile_id) REFERENCES consultant_platform.cand_profile (cand_profile_id)
);

CREATE INDEX IF NOT EXISTS idx_mission_slot_mission_id
    ON consultant_platform.mission_slot (mission_id);

CREATE INDEX IF NOT EXISTS idx_mission_slot_role_fill_status
    ON consultant_platform.mission_slot (role_id, mission_slot_fill_status);

CREATE INDEX IF NOT EXISTS idx_mission_slot_filled_by_profile_id
