    cand_profile_cv_id       BIGINT       NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    cand_profile_id          BIGINT       NOT NULL,
    cv_file_name             VARCHAR(255) NOT NULL,
    cv_content_type          VARCHAR(100) NOT NULL,
    cv_size_bytes            BIGINT       NULL,
    cv_extraction_pending    BOOLEAN      NOT NULL DEFAULT TRUE,
