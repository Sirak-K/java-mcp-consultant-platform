-- =============================================================================
-- V1 :: Baseline Schema - PostgreSQL
-- Consolidated baseline for VFAR competence matching.
-- Contains only active consultant-platform, runtime, and audit persistence.
-- =============================================================================

CREATE SCHEMA IF NOT EXISTS consultant_platform;
CREATE SCHEMA IF NOT EXISTS runtime;

-- -------------------------------------------------------------------------
-- consultant_platform.competency_level_lookup
-- -------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS consultant_platform.competency_level_lookup (
    competency_level_lookup_id SMALLINT    NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    competency_level_name      VARCHAR(20) NOT NULL,
    competency_min_years       SMALLINT    NOT NULL,
    PRIMARY KEY (competency_level_lookup_id),
    CONSTRAINT uk_competency_level_lookup_name UNIQUE (competency_level_name),
    CONSTRAINT uk_competency_level_lookup_min_years UNIQUE (competency_min_years),
    CONSTRAINT chk_competency_level_lookup_name_nonblank CHECK (CHAR_LENGTH(TRIM(competency_level_name)) > 0),
    CONSTRAINT chk_competency_level_lookup_min_years CHECK (competency_min_years >= 0)
);

-- -------------------------------------------------------------------------
-- consultant_platform.role
-- -------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS consultant_platform.role (
    role_id    BIGINT       NOT NULL GENERATED BY DEFAULT AS IDENTITY,
